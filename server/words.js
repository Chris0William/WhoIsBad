const DIFFICULTY_MAP = {
  easy: WORDS_EASY,
  normal: WORDS_NORMAL,
  hard: WORDS_HARD,
};

const DIFFICULTY_NAME = {
  easy: '简单',
  normal: '普通',
  hard: '困难',
};

function getRandomPair(difficulty) {
  const pool = DIFFICULTY_MAP[difficulty] || WORDS_NORMAL;
  const pair = pool[Math.floor(Math.random() * pool.length)];
  // 随机决定哪个是平民词哪个是卧底词
  if (Math.random() > 0.5) {
    return { civilianWord: pair[0], spyWord: pair[1] };
  }
  return { civilianWord: pair[1], spyWord: pair[0] };
}

